# 1. Bitwise AND (&)
a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a & b  # 0001 in binary (1 in decimal)
print(result)  # Output: 1
# 2. Bitwise OR (|)

a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a | b  # 0111 in binary (7 in decimal)
print(result)  # Output: 7
# 3. Bitwise XOR (^)

a = 5  # 0101 in binary
b = 3  # 0011 in binary
result = a ^ b  # 0110 in binary (6 in decimal)
print(result)  # Output: 6
# 4. Bitwise NOT (~)

a = 5  # 0101 in binary
result = ~a  # 1010 in binary (-6 in decimal)
print(result)  # Output: -6

# 5. Left Shift (<<)
a = 5  # 0101 in binary
result = a << 2  # 10100 in binary (20 in decimal)
print(result)  # Output: 20
# 6. Right Shift (>>)

a = 5  # 0101 in binary
result = a >> 2  # 0001 in binary (1 in decimal)
print(result)  # Output: 1

